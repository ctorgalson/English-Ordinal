/**
 * This function returns the incoming string with the appropriate English
 * language suffix appended. For example, given '3', it returns '3rd', given
 * '6' it returns '6th' etc.
 *
 * The important part of this function--the really, really clever bit--is
 * originally from Steven Levithan's excellent MIT-licensed, (and MUCH more
 * full-featured) date.format.js library. If you need a really flexible js date
 * formatting solution, you may want to use date.format.js instead:
 *
 * http://blog.stevenlevithan.com/archives/date-time-format#comment-24941
 *
 * Thanks Steven!
 *
 * Brought to my attention by @chx--Thanks @chx!
 * (http://twitter.com/#!/chx/status/149057216522092544).
 *
 * Might be used to make any number generated by javascript more human-friendly,
 * for example with dates:
 *
 * // Displays an alert that says "20th".
 * // Note that the function MUST be called on a string!
 * var d = new Date(2011, 11, 20).getDate();
 * alert(String(d).englishOrdinal());
 * 
 * @param string separator
 *    String to insert between the digit and the suffix--generally used to add
 *    a space.
 * @return string
 *    The incoming value, plus the separator (if any), plus the suffix.
 *
 * @author Steven Levithan
 * @author Christopher Torgalson <manager@bedlamhotel.com>
 *
 * @see http://blog.stevenlevithan.com/archives/date-time-format
 *
 * @version 1.1
 */
String.prototype.englishOrdinal = function(separator) {
  return this + (separator ? separator : '') + ['th', 'st', 'nd', 'rd'][d % 10 > 3 ? 0 : (d % 100 - d % 10 != 10) * d % 10];
}; /* String.prototype.englishOrdinal */